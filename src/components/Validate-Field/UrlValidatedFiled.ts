import { action, computed, observable } from 'mobx';
import { IValidatedFieldConfig, ValidatedField, IValidatorConfig } from 'mobx-validated-field';


export class UrlValidatedField extends ValidatedField {
    @observable private _prePend: string;

    constructor(config?: IValidatedFieldConfig) {
        super(config);

        this.addValidators([
            {
                id: 'valid_url',
                defaultMessage: 'Please enter a valid URL.',
                validateOnSubmit: (val: string) => {
                    return val == null || val === '' || URL_REGEX.test(this.prePend?this.prePend+val:`https://${val}`);
                }
            }
        ]);
    }

    @computed
    get prePend(): string {
        return this._prePend;
    }

    @action
    handlePrePendChange = (val: string) => {
        this._prePend = val;
    };

    handleChange(val: string) {
        super.handleChange(removePrePend(val));
    }
}

